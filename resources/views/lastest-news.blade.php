						<h3>Students offer free walking tours to foreign tourists in Da Nang</h3>
							<ul class="blog-meta">
								<li><i class="fa fa-user"></i>Tuoi Tre News</li>
								<li><i class="fa fa-clock-o"></i>Thursday, December 14, 2017, 11:25 GMT+7</li>
								<li><i class="fa fa-heart"></i>78</li>
							</ul>
						<div class="blog-img">
							<img class="img-responsive" src="assets/images/news1.jpg" alt="" style="width: 100%">
						</div>
						<div class="blog-content">
							<p><strong>The student-guided tours are primarily targeted at foreign tourists.</strong></p>

							<p>Based on a “pay what you think is fair” model, the ‘Danang Free Walking Tour’ project focuses on providing customers with full, satisfying, and local experiences that visitors might otherwise miss during their trip to Da Nang – one of central Vietnam’s main tourist hubs.</p>

							<p>Breathtaking scenery, unique sights, and beautiful beaches are the main draw to Vietnam’s third largest city, and the largest city in the central region.  </p>

							<p>The tours’ main goal is to help visitors experience the city from a local point of view, and hopefully walk away with a long-lasting positive impression.</p>

							<p>The program’s success is chiefly attributed to the enthusiasm and excitement of its tour guides – volunteer university students from institutions across Da Nang – and the diversity of the tourist sites customers are introduced to during the ninety-minute tour, including Han Market, Da Nang Cathedral, the Cham Sculpture Museum, and the Han River Bridge.</p>

							<p>XP and Kathy from Hong Kong are just two out of hundreds of international tourists impressed by the tour.</p>

							<p>The couple had nothing but praise for their energetic tour guide, Sofia, from the University of Foreign Languages in Da Nang.</p>

							<p>“Sofia is very passionate and knows a lot about the city. Her casual and friendly smile made the tour unforgettable,” Kathy said.</p>

							<p>“Thanks to her, we learnt a lot about the history, culture, and lives of people here. This city is truly beautiful and has really impressed us. There are many things that we would probably never have learned had it not been for her enthusiasm and understanding. I will certainly recommend this tour to my friends.”</p>

							<p>Le Thi Thu Hang, an English major at the University of Foreign Languages in Da Nang, is another volunteer who began working with the project in August 2017.</p>

							<p>Her preparation to become a tour guide included research into the city’s history, culture, and traditions so as to provide tourists with useful and interesting information they might not find elsewhere.</p>

							<p>“Thanks to this tour I have a chance to improve my language skills by speaking with foreigners, as well as learning about other countries’ cultures,” Hang said about her experience working for the ‘Danang Free Walking Tour.’ </p>

							<p>“I have introduced many interesting things about Da Nang to foreign tourists and most have come to the conclusion that this is a city worth living in for its beautiful scenery, good infrastructure, variety of foods, and friendly people.”</p>

							<p>Le Song Gia Han, another volunteer tour guide, has been with the project for nearly a year.  The foreign trade student at the University of Economics in Da Nang has already guided nearly around 100 tours.</p>

							<p>According to Han, volunteers stand to gain a lot from working with the program.</p>

							<p>She says that apart from the chance to improve foreign language skills, their understanding of tourism also improves considerably.</p>

							<p><strong>Difficult journey</strong></p>

							<p>The ‘Danang Free Walking Tour’ has become so popular that the organization now fields several requests from foreign tourists on a daily basis.</p>

							<p>During summer, Da Nang’s peak tourist season, the project operators often receive hundreds of requests. However, reaching that point was no walk in the park for its founder, Nguyen Minh Loi.</p>

							<p>The 26-year-old shared that the hardest part of achieving success was building people’s trust.</p>

							<p>“Our first try had a considerable number of volunteers participating but no customers joined the tours so the group quickly dissolved,” Loi recalled of the project’s starting phase.</p>

							<p>“There were times I would wander through intersections and touristic destinations to spread the word about the free service. However, most potential customers just avoided us.</p>

							<p>“We thought that it might be rude to approach customers directly, so we switched to advertising through posts, pictures, and videos on social media.</p>

							<p>“Eventually, many people began to learn about us and chose to participate. Thanks to their good reviews, many more people wanted to try our service.”</p>

							<p>Loi’s project is not limited to free walking tours. The entrepreneur has also expanded the project to include the ‘Foody Story Tour’ and ‘Your Planner’ – a service designed to help tourists plan tailored trips to fit their interests.</p>

							<p>While ‘Danang Free Walking Tour’ and ‘Your Planner’ are free, the ‘Foody Story Tour’ requires a fee of US$30 per person.</p>

							<p>The ‘Foody Story Tour’ guides visitors through well-known restaurants and unique cuisine around the city.</p>

							<p>The project also receives support from municipal authorities.</p>

							<p>According to the vice-director of Department of Tourism in Da Nang, Tran Chi Cuong, his agency helps advertise the service.</p>

							<p>Advertisements, leaflets and information about ‘Danang Free Walking Tour’ are provided at the airport and several tourist destinations, he said.</p>

							<p>“This is a new form of tourism, letting tourists see the lives of people in the city,” he added.</p>

							<p>“Tourists get a chance to experience Da Nang&#39;s cuisine, culture, and history under a very new perspective.</p>

							<p>“Moreover, by traveling on foot, the project is good for the environment and for sustainable development.”</p>

